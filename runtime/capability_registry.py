def get_capability_registry_issues() -> Dict[str, Any]:
    declared = set(get_all_declared_capabilities())
    bound = set(CAPABILITY_EXECUTION_BINDINGS.keys())
    governed = set(GOVERNED_CAPABILITY_PROFILES.keys())
    declarative = set(DECLARATIVE_READONLY_CAPABILITIES)
    roster_issues = get_roster_integrity_issues()
    executable_declared_without_binding = sorted((declared - bound) - declarative)
    declarative_without_binding = sorted((declared - bound) & declarative)
    return {
        "declared_without_binding": executable_declared_without_binding,
        "declarative_without_binding": declarative_without_binding,
        "binding_without_declared": sorted(bound - declared),
        "governed_without_binding": sorted(governed - bound),
        "write_capabilities": sorted(
            capability
            for capability, binding in CAPABILITY_EXECUTION_BINDINGS.items()
            if bool(binding.get("write"))
        ),
        "team_roster_missing_agents": roster_issues.get("team_roster_missing_agents", []),
        "agent_roster_missing_registry": roster_issues.get("agent_roster_missing_registry", []),
        "registry_missing_agent_roster": roster_issues.get("registry_missing_agent_roster", []),
    }



